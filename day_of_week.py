day = int(input('Day (1-7)? '))
if day == 1:
    print("Sunday")
elif day == 2:
    print("Monday")
elif day == 3:
    print("Tuesday")
elif day == 4:
    print("Humpday")
elif day == 5:
    print("Thursday")
elif day == 6:
    print("TGIFriday")
elif day == 7:
    print("Saturday")
else:
    print("How many days are in a week again?")